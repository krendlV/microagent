# Tests
# ---------------------------------------------------------------------------


@pytest.mark.integration
def test_full_pipeline_synthetic(tmp_path, synthetic_data):
    """Full pipeline: generate → inspect → segment → evaluate → report.

    Verifies:
    - HTML report is produced
    - Dataset-level F1 at IoU=0.5 is > 0.5 (reasonable quality)
    - No crashes throughout
    """
    from microagent.core.evaluate import evaluate_masks
    from microagent.core.inspect import inspect_directory
    from microagent.core.segment import run_segmentation
    from microagent.viz.report import generate_report, load_report_data

    base, image_dir, gt_dir = synthetic_data
    project_yaml = base / "project.yaml"

    # 1. Inspect -----------------------------------------------------------
    insp = inspect_directory(image_dir)
    assert insp.file_count == 5
    assert not any(issue.startswith("Failed") for issue in insp.issues), (
        f"Unexpected load failures: {insp.issues}"
    )
    insp_json = tmp_path / "inspection.json"
    insp.save_json(insp_json)

    # 2. Segment -----------------------------------------------------------
    masks_dir = tmp_path / "masks"
    masks_dir.mkdir()
    seg = run_segmentation(
        image_dir=image_dir,
        output_dir=masks_dir,
        model="cellpose",
        project_path=project_yaml if project_yaml.exists() else None,
    )
    assert len(seg.mask_paths) == 5
    assert all(Path(p).exists() for p in seg.mask_paths)
    seg_json = tmp_path / "segmentation.json"
    seg.save_json(seg_json)

    # 3. Evaluate ----------------------------------------------------------
    ev = evaluate_masks(
        pred_dir=masks_dir,
        gt_dir=gt_dir,
        thresholds=[0.5, 0.75],
        force_fallback=True,  # avoid optional stardist dependency
    )
    assert ev.summary.n_images == 5
    assert len(ev.unmatched_preds) == 0, (
        f"Unmatched predictions: {ev.unmatched_preds}"
    )
    f1 = _f1_at_05(ev)
    assert f1 > 0.5, (
        f"F1@0.5={f1:.3f} is too low — check synthetic data quality or "
        "CellPose diameter settings in project.yaml"
    )
    ev_json = tmp_path / "metrics.json"
    ev.save_json(ev_json)

    # 4. Report ------------------------------------------------------------
    report_path = tmp_path / "report.html"
    data = load_report_data(
        inspection_json=insp_json,
        segmentation_json=seg_json,
        evaluation_json=ev_json,
        project_yaml=project_yaml if project_yaml.exists() else None,
        command="integration_test_full_pipeline",
    )
    out = generate_report(data, report_path)

    assert out == report_path
    assert report_path.exists()
    html = report_path.read_text(encoding="utf-8")
    assert len(html) > 500, "Report HTML seems too short"
    assert "microagent" in html.lower(), "Report should mention microagent"


