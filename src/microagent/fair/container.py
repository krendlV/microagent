def _pip_freeze() -> list[str]:
    """Return lines from ``pip freeze`` (or importlib fallback)."""
    try:
        result = subprocess.check_output(
            [sys.executable, "-m", "pip", "freeze"],
            stderr=subprocess.DEVNULL,
            text=True,
        )
        return [line for line in result.splitlines() if line and not line.startswith("#")]
    except Exception:
        # Fallback: enumerate installed distributions
        dists = importlib.metadata.packages_distributions()
        seen: set[str] = set()
        lines: list[str] = []
        for pkg in sorted(dists):
            try:
                ver = importlib.metadata.version(pkg)
                entry = f"{pkg}=={ver}"
                if entry not in seen:
                    seen.add(entry)
                    lines.append(entry)
            except Exception:
                pass
        return lines


def _cuda_base_image(cuda_version: str | None) -> str:
    """Choose the best matching CUDA base image tag."""
    if cuda_version is None:
        return "python:3.11-slim"
    # Map CUDA X.Y → nvidia/cuda:X.Y.z-runtime-ubuntu22.04
    # We only pin the major.minor and use a widely available patch.
    major_minor = ".".join(cuda_version.split(".")[:2])
    known = {
        "12.1": "12.1.0",
        "12.2": "12.2.0",
        "12.3": "12.3.0",
        "11.8": "11.8.0",
        "11.7": "11.7.1",
    }
    patch = known.get(major_minor, f"{major_minor}.0")
    return f"nvidia/cuda:{patch}-runtime-ubuntu22.04"


