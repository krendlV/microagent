"""End-to-end integration tests for the full MicroAgent pipeline.

All tests use synthetic data generated by microagent.demo.synthetic and
run without real microscopy images or GPU access.

Run with:
    uv run pytest tests/integration/ -m integration -v
"""

from __future__ import annotations

from pathlib import Path

import pytest

# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------


def _f1_at_05(eval_result) -> float:
    """Return the dataset-level F1 at IoU=0.5 threshold."""
    for tm in eval_result.summary.per_threshold:
        if abs(tm.threshold - 0.5) < 1e-9:
            return tm.f1
    return eval_result.summary.per_threshold[0].f1


